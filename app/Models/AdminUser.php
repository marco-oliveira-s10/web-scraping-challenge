<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;

class AdminUser extends Authenticatable
{
    use HasFactory, Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'role',
        'settings',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
        'password' => 'hashed',
        'settings' => 'json',
    ];
    
    /**
     * Get the admin's notification preferences.
     */
    public function receivesBatchNotifications()
    {
        return $this->settings['receive_batch_notifications'] ?? true;
    }
    
    /**
     * Get the admin's critical notification preferences.
     */
    public function receivesCriticalNotifications()
    {
        return $this->settings['receive_critical_notifications'] ?? true;
    }
}